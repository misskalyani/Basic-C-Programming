#include<iostream>
using namespace std;
int main()
{
	int a;
	cout<<"Enter Digit : ";
	cin>>a;
	if(a==0)
	{
		cout<<"ZERO";
	}
	else if(a==1)
	{
		cout<<"ONE";
	}
	else if(a==2)
	{
		cout<<"TWO";
	}
	else if(a==3)
	{
		cout<<"THREE";
	}
	else if(a==4)
	{
		cout<<"FOUR";
	}
	else if(a==5)
	{
		cout<<"FIVE";
	}
	else if(a==6)
	{
		cout<<"SIX";
	}
	else if(a==7)
	{
		cout<<"SEVEN";
	}
	else if(a==8)
	{
		cout<<"EIGHT";
	}
	else if(a==9)
	{
		cout<<"NINE";
	}
	else
	{
		cout<<"Invalid Case";
	}
}
